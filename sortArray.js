// Basic sort array
// what is sorting

const arrayOfNumbers = [5, 3, 8, 1, 2];
const arrayOfStrings = ['banana', 'apple', 'orange', 'grape'];


// Sort numbers in ascending order
console.log(arrayOfNumbers) //main array
const sortedNumbers = arrayOfNumbers.sort();
console.log(sortedNumbers); // Output: [1, 2, 3, 5, 8]


// Sort strings in alphabetical order
console.log(arrayOfStrings) //main array
const sortedStrings = arrayOfStrings.sort();
console.log(sortedStrings); // Output: ['apple', 'banana', 'grape', 'orange']

